SELECT DEPARTMENT_ID
FROM DEPARTMENTS
MINUS
SELECT DEPARTMENT_ID
FROM EMPLOYEES
WHERE JOB_ID='ST_CLERK';

REM --2ND QUESTION

SELECT COUNTRY_ID,COUNTRY_NAME
FROM COUNTRIES
MINUS
SELECT L.COUNTRY_ID,C.COUNTRY_NAME
FROM LOCATIONS L
JOIN COUNTRIES C
ON (L.COUNTRY_ID= C.COUNTRY_ID)
JOIN DEPARTMENTS D
ON(D.LOCATION_ID=L.LOCATION_ID);


