FROM EMPLOYEES
WHERE DEPARTMENT_ID=10
UNION ALL
SELECT DISTINCT JOB_ID,DEPARTMENT_ID,TO_CHAR('Z') AS "DUMMY"
FROM EMPLOYEES
WHERE DEPARTMENT_ID=20 
UNION ALL
SELECT DISTINCT JOB_ID,DEPARTMENT_ID,TO_CHAR('Y') AS "DUMMY"
FROM EMPLOYEES
WHERE DEPARTMENT_ID=50 ;

